def transcrever(dna):
	''' 
	* Converte DNA => RNA 
	* 1. descobrir par
	*    A => U
	*    T => A
	*    C => G
	*    G => C
	* 2. inverter seq
	'''

	rna = ''

	# para cada nucleotídeo do DNA
	for i in range(len(dna)):

		# converte e inverte

		if dna[i] == 'a':
			rna = 'u' + rna
		elif dna[i] == 't':
			rna = 'a' + rna
		elif dna[i] == 'c':
			rna = 'g' + rna	
		elif dna[i] == 'g':
			rna = 'c' + rna

	return rna

